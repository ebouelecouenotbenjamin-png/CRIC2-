
document.addEventListener('DOMContentLoaded', function() {

  // 1. On sélectionne les éléments HTML dont on a besoin
  const bouton = document.querySelector('.plus');
  const contenu = document.querySelector('.mon-contenu');

  // 2. On s'assure que les éléments existent avant d'ajouter un écouteur
  if (bouton && contenu) {
    
    // 3. On écoute l'événement 'click' sur le bouton
    bouton.addEventListener('click', function() {
      
      // 4. On bascule la classe 'visible' sur le contenu
      contenu.classList.toggle('visible');

      // 5. On met à jour le texte du bouton en fonction de l'état
      if (contenu.classList.contains('visible')) {
        // Si le contenu est visible, on affiche "moins"
        bouton.textContent = 'moins';
      } else {
        // Sinon, on affiche "plus"
        bouton.textContent = 'plus';
      }
    });
     }
});
  
